package com.wangguangwu.prototype;

/**
 * 原型接口：定义克隆方法
 * <p>
 * 原型模式（Prototype Pattern）是一种创建型设计模式，它允许你通过复制现有对象来创建新对象，
 * 而不是通过常规的构造函数。
 * <p>
 * 主要优点：
 * 1. 性能提升，避免了重复的创建过程
 * 2. 可以动态创建对象
 * 3. 可以指定要创建的对象的种类
 * <p>
 * 主要缺点：
 * 1. 每个原型类都需要实现克隆方法
 * 2. 如果对象包含循环引用，克隆可能会很复杂
 * <p>
 * 适用场景：
 * 1. 当创建对象的过程比较复杂时
 * 2. 当需要创建大量相似对象时
 * 3. 当对象的创建依赖于动态数据时
 *
 * @author wangguangwu
 */
public interface Prototype {

    /**
     * 克隆对象
     *
     * @return 克隆的对象
     */
    Prototype clone();

}